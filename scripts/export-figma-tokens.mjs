#!/usr/bin/env node
// Exports Gestral UI's design tokens (colors, text scale, radius scale,
// spacing scale, icon sizes) as a Tokens Studio-compatible JSON file, so
// they can be pushed into Figma Variables (via the Tokens Studio plugin's
// GitHub/URL sync, or a follow-up script that calls the Figma REST
// Variables API directly).
//
// Source of truth stays in code — nothing here is hand-maintained, and
// every value is read straight from build output:
//   - src/styles/tokens.css       hand-authored semantic color mapping,
//                                  light (:root) + dark (@media) — see
//                                  CLAUDE.md.
//   - src/styles/generated.css    text/leading/radius/spacing scale values,
//                                  generated by src/lib/generateTokensCss.ts
//                                  (its plain `:root` block only — the
//                                  `@theme inline` block below it is a
//                                  self-referential Tailwind mirror, not a
//                                  value source).
//   - dist/index.css              the fully built stylesheet, used only to
//                                  resolve raw Tailwind palette leaves
//                                  (--color-stone-900, --text-9xl, the base
//                                  --spacing unit, ...) to concrete values.
//
// Requires `pnpm build` to have been run first (dist/index.css and
// src/styles/generated.css are both build artifacts).
//
// Usage: pnpm build && node scripts/export-figma-tokens.mjs [outFile]

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { converter } from 'culori';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const oklchToRgb = converter('rgb');

/** Extracts every `--name: value;` custom-property declaration from a flat (unnested) CSS block. */
export function extractDeclarations(css) {
  const declarations = new Map();
  for (const match of css.matchAll(/(--[a-zA-Z0-9-]+)\s*:\s*([^;{}]+);/g)) {
    const [, name, value] = match;
    if (!declarations.has(name)) declarations.set(name, value.trim());
  }
  return declarations;
}

/** Pulls the Tailwind palette + base scale vars out of a built stylesheet's `@layer theme{:root,:host{...}}` block. */
export function extractBuiltTheme(builtCss) {
  const match = builtCss.match(/@layer theme\{:root,:host\{([^}]*)\}\}?/);
  if (!match) throw new Error('Could not find `@layer theme{:root,:host{...}}` in the built stylesheet.');
  return extractDeclarations(match[1]);
}

/** Splits tokens.css's hand-authored source into its light (:root) and dark (@media ... :root) declaration maps. */
export function splitLightDark(tokensCss) {
  const darkMatch = tokensCss.match(/@media \(prefers-color-scheme:\s*dark\)\s*\{\s*:root\s*\{([\s\S]*?)\n  \}\n\}/);
  const lightOnly = darkMatch ? tokensCss.slice(0, darkMatch.index) : tokensCss;
  return {
    light: extractDeclarations(lightOnly),
    dark: darkMatch ? extractDeclarations(darkMatch[1]) : new Map(),
  };
}

/** Pulls the plain, non-circular `:root { ... }` block out of generated.css (the block before `@theme inline`). */
export function extractGeneratedRoot(generatedCss) {
  const themeIndex = generatedCss.indexOf('@theme inline');
  const rootOnly = themeIndex === -1 ? generatedCss : generatedCss.slice(0, themeIndex);
  return extractDeclarations(rootOnly);
}

/** Resolves a `var(--x)` chain (or literal) against one or more declaration maps, in priority order. */
export function resolveVar(value, ...maps) {
  let current = value;
  for (let i = 0; i < 10; i++) {
    const match = current.match(/^var\((--[a-zA-Z0-9-]+)\)$/);
    if (!match) return current;
    const name = match[1];
    const found = maps.map((m) => m.get(name)).find((v) => v !== undefined);
    if (found === undefined) throw new Error(`Unresolved token reference: ${name}`);
    current = found;
  }
  throw new Error(`var() chain too deep resolving: ${value}`);
}

/** Converts a resolved CSS color (oklch(...), hex, or any culori-parseable format) to Figma's {r,g,b,a} 0..1 shape. */
export function toFigmaColor(cssColor) {
  const rgb = oklchToRgb(cssColor);
  if (!rgb) throw new Error(`Could not parse color: ${cssColor}`);
  return { r: clamp01(rgb.r), g: clamp01(rgb.g), b: clamp01(rgb.b), a: rgb.alpha ?? 1 };
}

function clamp01(n) {
  return Math.min(1, Math.max(0, n));
}

/** rem string (e.g. "1.5rem") -> px number, assuming a 16px root (this repo never overrides html font-size). */
function remToPx(rem) {
  const match = rem.match(/^(-?[\d.]+)rem$/);
  if (!match) throw new Error(`Expected a rem value, got: ${rem}`);
  return parseFloat(match[1]) * 16;
}

/** A spacing-scale expression (`calc(var(--spacing) * N)`, `--spacing(N)`, or a literal `Npx`) -> px number. */
function spacingExprToPx(expr, baseSpacingPx) {
  const calcMatch = expr.match(/^calc\(var\(--spacing\)\s*\*\s*([\d.]+)\)$/);
  if (calcMatch) return baseSpacingPx * parseFloat(calcMatch[1]);
  const fnMatch = expr.match(/^--spacing\(([\d.]+)\)$/);
  if (fnMatch) return baseSpacingPx * parseFloat(fnMatch[1]);
  const pxMatch = expr.match(/^(-?[\d.]+)px$/);
  if (pxMatch) return parseFloat(pxMatch[1]);
  throw new Error(`Unrecognized spacing expression: ${expr}`);
}

/**
 * Builds a Tokens Studio-format token set from tokens.css (semantic color
 * mapping), generated.css (text/radius/spacing scale), and the built
 * stylesheet (Tailwind palette leaves).
 */
export function buildTokenSet({ builtCss, tokensCss, generatedCss }) {
  const palette = extractBuiltTheme(builtCss);
  const { light, dark } = splitLightDark(tokensCss);
  const generatedRoot = extractGeneratedRoot(generatedCss);
  const baseSpacingPx = remToPx(palette.get('--spacing') ?? (() => { throw new Error('Could not find base --spacing in the built stylesheet.'); })());

  const colorNames = new Set([...light.keys(), ...dark.keys()].filter((n) => n.startsWith('--color-')));
  const color = {};
  for (const name of colorNames) {
    const slug = name.replace('--color-', '');
    const lightValue = light.get(name);
    const darkValue = dark.get(name) ?? lightValue;
    color[slug] = {
      light: toFigmaColor(resolveVar(lightValue, light, palette)),
      dark: toFigmaColor(resolveVar(darkValue, dark, light, palette)),
    };
  }

  const fontSize = {};
  for (const [name, value] of generatedRoot) {
    if (!name.startsWith('--text-') || name.endsWith('--line-height')) continue;
    const slug = name.replace('--text-', '');
    fontSize[slug] = remToPx(resolveVar(value, generatedRoot, palette));
  }

  const borderRadius = {};
  for (const [name, value] of generatedRoot) {
    if (!name.startsWith('--radius-')) continue;
    const slug = name.replace('--radius-', '');
    borderRadius[slug] = value === '9999px' ? 9999 : parseFloat(value);
  }

  const spacing = {};
  for (const [name, value] of generatedRoot) {
    if (!name.startsWith('--spacing-sp-')) continue;
    const slug = name.replace('--spacing-', '');
    spacing[slug] = spacingExprToPx(value, baseSpacingPx);
  }

  const iconSize = {};
  for (const [name, value] of light) {
    if (!name.startsWith('--icon-size-')) continue;
    const slug = name.replace('--icon-size-', '');
    iconSize[slug] = spacingExprToPx(value, baseSpacingPx);
  }

  return { color, fontSize, borderRadius, spacing, iconSize };
}

async function main() {
  const builtCssPath = resolve(ROOT, 'dist/index.css');
  const tokensCssPath = resolve(ROOT, 'src/styles/tokens.css');
  const generatedCssPath = resolve(ROOT, 'src/styles/generated.css');
  const outPath = resolve(ROOT, process.argv[2] ?? 'figma-tokens.json');

  const readOrExplain = (path) => {
    try {
      return readFileSync(path, 'utf-8');
    } catch {
      throw new Error(`${path} not found — run \`pnpm build\` first so tokens are resolved to concrete values.`);
    }
  };

  const tokenSet = buildTokenSet({
    builtCss: readOrExplain(builtCssPath),
    tokensCss: readOrExplain(tokensCssPath),
    generatedCss: readOrExplain(generatedCssPath),
  });

  writeFileSync(outPath, JSON.stringify(tokenSet, null, 2) + '\n');
  console.log(
    `Wrote ${Object.keys(tokenSet.color).length} colors, ${Object.keys(tokenSet.fontSize).length} font sizes, ` +
      `${Object.keys(tokenSet.borderRadius).length} radii, ${Object.keys(tokenSet.spacing).length} spacing steps, ` +
      `${Object.keys(tokenSet.iconSize).length} icon sizes -> ${outPath}`,
  );
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main().catch((err) => {
    console.error(err.message);
    process.exit(1);
  });
}
